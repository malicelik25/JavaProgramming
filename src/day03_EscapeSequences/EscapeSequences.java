package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote
   \n: starts a new line
   \t: (tab) button (paragraph space)
   \\: single back slash
   \": double quote
 */

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Java \nPython \nC#");

        System.out.println("------------------------------------");
        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("-----------------------------------");
        System.out.println("\tJava is Cool");

        System.out.println("-------------------------------------");

        System.out.println("/ \\"); // in order to print one back slash we need to give two backword slashes

        System.out.println("My favorite TV show is \"Game Of Throne\""); // in order to print double quote, we need to use \"


    }





}
