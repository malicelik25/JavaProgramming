package day05_Concatenation;

public class Concatenation {

    public static void main(String[] args) {

        int year = 2021;
        String make = "Renault";
        String model = "Clio";
        int miles = 250000;
        String color = "White";
        int price = 350000;


        System.out.println(year + " " + make + " " + model + ", " + miles + " " + "miles" + ", " +
                color + ", " + price + ".");


    }
}